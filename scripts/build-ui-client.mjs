import { writeFile, mkdir, readFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import esbuild from 'esbuild';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const outputPath = path.join(repoRoot, 'src/generated/ui-client.ts');

async function buildClientBundle() {
  const result = await esbuild.build({
    entryPoints: [path.join(repoRoot, 'src/ui-react/main.tsx')],
    bundle: true,
    format: 'esm',
    platform: 'browser',
    target: 'es2022',
    write: false,
    sourcemap: false,
    minify: true,
    define: {
      'process.env.NODE_ENV': '"production"',
    },
    loader: {
      '.css': 'text',
      '.ttf': 'dataurl',
    },
    logLevel: 'silent',
  });

  const output = result.outputFiles?.[0]?.text;
  if (!output) throw new Error('No output generated for the React UI bundle.');

  const monacoCssPath = path.join(repoRoot, 'node_modules/monaco-editor/min/vs/editor/editor.main.css');
  const monacoCss = await readFile(monacoCssPath, 'utf8').catch(() => '');
  const styleBoot = monacoCss
    ? `(()=>{const style=document.createElement("style");style.textContent=${JSON.stringify(monacoCss)};document.head.appendChild(style);})();\n`
    : '';
  const bundle = `${styleBoot}${output.trim()}`;

  const contents = `// Generated by scripts/build-ui-client.mjs. Do not edit manually.
export const UI_CLIENT_BUNDLE = ${JSON.stringify(bundle)};
`;

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, contents, 'utf8');
}

await buildClientBundle();
