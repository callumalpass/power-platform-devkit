import { writeFile, mkdir } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import esbuild from 'esbuild';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const outputPath = path.join(repoRoot, 'src/generated/ui-vendor.ts');

const specifiers = [
  '@codemirror/autocomplete',
  '@codemirror/commands',
  '@codemirror/lang-xml',
  '@codemirror/language',
  '@codemirror/lint',
  '@codemirror/search',
  '@codemirror/state',
  '@codemirror/view',
  '@lezer/common',
  '@lezer/highlight',
  '@lezer/lr',
  '@lezer/xml',
  '@marijn/find-cluster-break',
  '@replit/codemirror-vim',
  'crelt',
  'style-mod',
  'w3c-keyname',
];

async function buildVendorBundles() {
  const modules = [];
  for (const specifier of specifiers) {
    const result = await esbuild.build({
      entryPoints: [specifier],
      bundle: true,
      format: 'esm',
      platform: 'browser',
      target: 'es2022',
      write: false,
      minify: false,
      sourcemap: false,
      logLevel: 'silent',
    });
    const output = result.outputFiles?.[0]?.text;
    if (!output) throw new Error(`No output generated for ${specifier}.`);
    modules.push([specifier, output.trim()]);
  }

  const contents = `// Generated by scripts/build-ui-vendor.mjs. Do not edit manually.
export const UI_VENDOR_MODULES: Record<string, string> = {
${modules.map(([specifier, source]) => `  ${JSON.stringify(`/assets/vendor/${specifier}`)}: ${JSON.stringify(source)},`).join('\n')}
};
`;

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, contents, 'utf8');
}

await buildVendorBundles();
